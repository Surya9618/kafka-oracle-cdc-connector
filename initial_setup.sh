ALTER DATABASE ADD SUPPLEMENTAL LOG DATA (ALL) COLUMNS;

GRANT SELECT ON V_$LOGMNR_CONTENTS TO C##DBUSER;
GRANT EXECUTE ON DBMS_LOGMNR TO C##DBUSER;
GRANT EXECUTE ON DBMS_LOGMNR_D TO C##DBUSER;

EXIT;
EOF

