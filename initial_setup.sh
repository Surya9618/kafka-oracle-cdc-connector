GRANT CREATE TABLE TO C##DBUSER CONTAINER=ALL;
GRANT CREATE SEQUENCE TO C##DBUSER CONTAINER=ALL;
GRANT CREATE TRIGGER TO C##DBUSER CONTAINER=ALL;
GRANT FLASHBACK ANY TABLE TO C##DBUSER CONTAINER=ALL;

